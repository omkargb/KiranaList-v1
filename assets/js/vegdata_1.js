var idCounter = 0;
$(document).on('click', "[class*=vegcopy]", function () {
    var row = $(this).closest('li');
    if(typeof(row.attr('clone-id')) != 'undefined') {
    	secondTable.find('li[clone-id=\''+ row.attr('clone-id') +'\']').remove();
      row.removeAttr('clone-id');
    } else {
    	row.attr('clone-id', idCounter);
      var clone = row.clone();
      secondTable.append(clone);
      idCounter++;
    }
});

